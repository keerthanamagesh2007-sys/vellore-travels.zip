<h1>St. John's <span>Church</span></h1>
<p>St. John's Church is one of the most famous and historically significant churches in Vellore. Built in 1846 by the British East India Company, it is recognized as one of the oldest churches in the Vellore Diocese. It stands as a silent witness to the rich colonial history of the region.</p>
<p><img src="images/st_johns_church.png" style="float:right; margin:5px 0px 5px 15px; width: 300px; border-radius: var(--radius-md); box-shadow: var(--shadow-md); transition: var(--transition);" alt="St. John's Church" />The church is highly regarded for its classic English Neo-Gothic architectural style. It features impressive towering spires, intricate stone carvings, and magnificent stained-glass windows depicting biblical stories that filter the sunlight into a beautiful array of colors.</p>
<p>St. John's Church is situated inside the historic Vellore Fort complex. Its location within the fort is a unique example of India's secular heritage, as the fort also houses the historic Jalakandeswarar Temple and a grand mosque. Visitors exploring the fort's robust granite ramparts and deep moat often stop at the church to experience its serene and peaceful atmosphere.</p>
<p>The church has played an active role in community service, education, and healthcare in the Vellore district. Its proximity to other major attractions makes it a must-visit destination for history enthusiasts, architecture lovers, and travelers seeking spiritual tranquility.</p>
